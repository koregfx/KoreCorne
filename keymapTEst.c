    /* Print current layer */
    oled_write("LAYER", false);

    oled_set_cursor(0,6);

    switch (get_highest_layer(layer_state)) {
        case _QWERTY:
            oled_write("Base ", false);
            break;
        case _ISAAC:
            oled_write("Isaac", false);
            break;
        case _WORK:
            oled_write("Work", false);
            break;
        case _NUMBER:
            oled_write("Numb", false);
            break;
        case _SIMBOL:
            oled_write("Simb", false);
            break;
        case _ADJUST:
            oled_write("Adj  ", false);
            break;
        default:
            oled_write("Undef", false);
    }

    /* caps lock */
    oled_set_cursor(0,8);
    // oled_write("CPSLK", led_usb_state.caps_lock);
    render_luna(0,13);
